
/* create database */
DROP DATABASE perpustakaan;
CREATE DATABASE perpustakaan;

/*  switch to the perpustakaan database */
USE perpustakaan;

/* create table buku */
CREATE TABLE buku (
	kodeBuku varchar(15) NOT NULL,
	judul varchar(75) NOT NULL,
	pengarang varchar(50) DEFAULT NULL,
	penerbit varchar(40) DEFAULT NULL,
	tahunTerbit decimal(4,0) NOT NULL,
	jumlahHalaman decimal (3,0) NOT NULL,
	bahasa varchar(15) DEFAULT NULL,
	PRIMARY KEY (kodeBuku)
);

CREATE TABLE kategori (
	idKategori varchar(20) NOT NULL,
	namaKategori varchar(25) NOT NULL,
	PRIMARY KEY (idKategori)
);

CREATE TABLE klasifikasi (
	noKlasifikasi varchar(15) NOT NULL,
	kodeBuku varchar(15) NOT NULL,
	idKategori varchar(20) NOT NULL,
	PRIMARY KEY (noKlasifikasi),
	FOREIGN KEY (kodeBuku) REFERENCES buku (kodeBuku),
	FOREIGN KEY (idKategori) REFERENCES kategori (idKategori)
);

CREATE TABLE rak (
	kodeRak varchar(10) NOT NULL,
	nomorRak decimal(10,0) NOT NULL,
	lokasiRak varchar(25) NOT NULL,
	PRIMARY KEY (kodeRak)
);

CREATE TABLE eksemplar (
	idEksemplar varchar(15) NOT NULL,
	kodeBuku varchar(15) NOT NULL,
	kodeRak varchar(10) NOT NULL,
	statusEksemplar varchar(15) DEFAULT NULL,
	PRIMARY KEY (idEksemplar),
	FOREIGN KEY (kodeBuku) REFERENCES buku (kodeBuku),
	FOREIGN KEY (kodeRak) REFERENCES rak (kodeRak)
);

CREATE TABLE users (
	userId varchar(20) NOT NULL,
	nama varchar(15) NOT NULL,
	noTelp varchar(15) DEFAULT NULL,
	jenisKelamin varchar(25) DEFAULT NULL,
	email varchar(50) DEFAULT NULL,
	alamat text,
	PRIMARY KEY (userId)
);

CREATE TABLE petugas (
	idPetugas varchar(10) NOT NULL,
	jabatan varchar(30) NOT NULL,
	PRIMARY KEY (idPetugas),
	FOREIGN KEY (idPetugas) REFERENCES users (userId)
);

CREATE TABLE member(
	idMember varchar(15) NOT NULL,
	statusAkun varchar(15) NOT NULL,
	totalPeminjamanBuku decimal(10,0) DEFAULT NULL,
	PRIMARY KEY (idMember),
	FOREIGN KEY (idMember) REFERENCES users (userId)
);

CREATE TABLE peminjaman (
	kodePinjam varchar(15) NOT NULL,
	idPetugas varchar(10) NOT NULL,
	tanggalPinjam date NOT NULL,
	tanggalJatuhTempo date NOT NULL,
	tanggalKembali date NOT NULL,
	statusPeminjaman varchar(15) NOT NULL,
	totalDenda decimal(10,0) DEFAULT NULL,
	PRIMARY KEY (kodePinjam),
	FOREIGN KEY (idPetugas) REFERENCES petugas (idPetugas)
);

CREATE TABLE logpinjaman (
	noLogPinjam varchar(15) NOT NULL,
	idEksemplar varchar (15) NOT NULL,
	kodePinjam varchar(15) NOT NULL,
	PRIMARY KEY (noLogPinjam),
	FOREIGN KEY (idEksemplar) REFERENCES eksemplar (idEksemplar),
	FOREIGN KEY (kodePinjam) REFERENCES peminjaman (kodePinjam)
);

CREATE TABLE detailpinjam (
	noDetailPinjam varchar(15) NOT NULL,
	kodePinjam varchar(15) NOT NULL,
	idMember varchar(15) NOT NULL,
	PRIMARY KEY (noDetailPinjam),
	FOREIGN KEY (kodePinjam) REFERENCES peminjaman (kodePinjam),
	FOREIGN KEY (idMember) REFERENCES member (idMember)
);

CREATE TABLE denda (
	idDenda varchar(10) NOT NULL,
	kodePinjam varchar(15) NOT NULL,
	jenisDenda varchar(20) DEFAULT NULL,
	jumlahDenda decimal(10,0) DEFAULT NULL,
	statusDenda varchar(15) DEFAULT NULL,
	tanggalPembayaran date DEFAULT NULL,
	metodePembayaran varchar(10) DEFAULT NULL,
	PRIMARY KEY (idDenda),
	FOREIGN KEY (kodePinjam) REFERENCES peminjaman (kodePinjam)
);

